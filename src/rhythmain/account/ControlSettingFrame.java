    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here
        try {

            DBConnect db = new DBConnect();

            Connection conn = db.con;

            String keyBindings
                    = txtUp.getText()
                    + txtDown.getText()
                    + txtLeft.getText()
                    + txtRight.getText();

            double speed
                    = sliderSpeed.getValue() / 10.0;

            String sql
                    = "UPDATE users SET "
                    + "key_bindings=?, "
                    + "scroll_speed=? "
                    + "WHERE user_id=?";

            PreparedStatement pst
                    = conn.prepareStatement(sql);

            pst.setString(
                    1,
                    keyBindings
            );

            pst.setDouble(
                    2,
                    speed
            );

            pst.setInt(
                    3,
                    UserSession.userId
            );

            pst.executeUpdate();

            JOptionPane.showMessageDialog(
                    null,
                    "Control berhasil disimpan!"
            );

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    null,
                    e
            );

        }

    }//GEN-LAST:event_btnSaveActionPerformed

