    private void loadControl() {

        try {

            DBConnect db = new DBConnect();

            Connection conn = db.con;

            String sql
                    = "SELECT * FROM users WHERE user_id=?";

            PreparedStatement pst
                    = conn.prepareStatement(sql);

            pst.setInt(
                    1,
                    UserSession.userId
            );

            ResultSet rs
                    = pst.executeQuery();

            if (rs.next()) {

                String key
                        = rs.getString("key_bindings");

                txtUp.setText(
                        String.valueOf(key.charAt(0))
                );

                txtDown.setText(
                        String.valueOf(key.charAt(1))
                );

                txtLeft.setText(
                        String.valueOf(key.charAt(2))
                );

                txtRight.setText(
                        String.valueOf(key.charAt(3))
                );

                sliderSpeed.setValue(
                        (int) (rs.getDouble("scroll_speed")
                        * 10)
                );

            }

        } catch (Exception e) {

            System.out.println(e);

        }

    }

